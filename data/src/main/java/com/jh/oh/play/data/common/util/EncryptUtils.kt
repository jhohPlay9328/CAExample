    fun removeWhiteSpace(originalString: String) =
        originalString.replace("\n", "").replace("\r", "")

    private fun removeUTFCharacters(data: String): String {
        val p = Pattern.compile("\\\\u(\\p{XDigit}{4})")
        val m = p.matcher(data)
        val buf = StringBuffer(data.length)
        while (m.find()) {
            val ch = m.group(1)!!.toInt(16).toChar().toString()
            m.appendReplacement(buf, Matcher.quoteReplacement(ch))
        }
        m.appendTail(buf)
        return buf.toString()
    }

    fun base64AESEncode(str: String, keyIndex: Int): String {
        val removeStr = com.jh.oh.play.data.common.util.EncryptUtils.removeUTFCharacters(str)
        Log.d("OG", "Retrofit Test createRequestBody jsonString $removeStr")
        val textBytes = removeStr.toByteArray(charset("UTF-8"))
        val ivSpec: AlgorithmParameterSpec = IvParameterSpec(com.jh.oh.play.data.common.util.EncryptUtils.ivSpec)
        val newKey = SecretKeySpec(com.jh.oh.play.data.common.util.EncryptUtils.mKeys[keyIndex - 1].toByteArray(charset("UTF-8")), "AES" )
        val cipher = Cipher.getInstance("AES/CBC/PKCS5Padding")
        cipher.init(Cipher.ENCRYPT_MODE, newKey, ivSpec)
        return Base64.encodeToString(cipher.doFinal(textBytes), Base64.DEFAULT)
    }

    fun base64AESDecode(str: String?, key: String): String {
        val textBytes = Base64.decode(str, Base64.DEFAULT)
        val ivSpec: AlgorithmParameterSpec = IvParameterSpec(com.jh.oh.play.data.common.util.EncryptUtils.ivSpec)
        val newKey = SecretKeySpec(key.toByteArray(charset("UTF-8")), "AES")
        val cipher = Cipher.getInstance("AES/CBC/PKCS5Padding")
        cipher.init(Cipher.DECRYPT_MODE, newKey, ivSpec)
        return cipher.doFinal(textBytes).toString(charset("UTF-8"))
    }

    /**
     * Preference 아이디/비번/잠금비번 AES256 암호화
     * @param key
     * @param value
     * @return
     * @throws Exception
     */
    @Throws(Exception::class)
    fun encodeAES(key: String, value: String): String? {
        val secretKeySpec = SecretKeySpec(key.toByteArray(), "AES")
        val cipher = Cipher.getInstance("AES/CBC/PKCS5Padding")
        cipher.init(
            Cipher.ENCRYPT_MODE,
            secretKeySpec,
            IvParameterSpec(com.jh.oh.play.data.common.util.EncryptUtils.ivSpec)
        )
        val randomKey = cipher.doFinal(value.toByteArray())
        return Base64.encodeToString(randomKey, 0)
    }
